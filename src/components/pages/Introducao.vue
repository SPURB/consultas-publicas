			<h2 class="subtitle">PIU - Racional da Política Pública </h2>
			<p>Os terminais de ônibus constituem importantes equipamentos de transferência e conexão da rede de transporte coletivo urbano do Município de São Paulo<sup><a href="#fn1">1</a></sup>. Nos 27 (vinte e sete) terminais de ônibus municipais embarcam diariamente 905 (novecentos e cinco) mil passageiros e passa grande parte da frota de ônibus Municipal<sup><a href="#fn2">2</a></sup>. Esse grande fluxo de veículos e usuários, que se desloca para os bairros ou para o centro da cidade, confere aos terminais o potencial de se tornarem importantes vetores do desenvolvimento urbano e econômico da região em que estão inseridos.</p>
			<p>A identificação do potencial dos terminais de ônibus para o desenvolvimento urbano do Município é evidenciada no Plano Diretor Estratégico de São Paulo, que introduz como um de seus objetivos básicos acomodar o crescimento urbano nas áreas subutilizadas dotadas de infraestrutura e no entorno da rede de transporte coletivo de alta e média capacidade<sup><a href="#fn3">3</a></sup>. Para alcançar esse objetivo, o referido plano define como estratégia o estimulo à criação de novas centralidades e a dinamização das existentes, em muitos casos, através da exploração de terminais como elementos catalisadores do desenvolvimento de comércio, serviços públicos e privados e de outras atividades econômicas admitidas no próprio imóvel ou em seu entorno, pela regulamentação aplicável<sup><a href="#fn4">4</a></sup>.</p>
			<p>Em linha com as diretrizes do PDE, o Plano de Mobilidade Urbana, instituído pelo Decreto Municipal nº 56.834/ 2016, estabelece a “inclusão, nos projetos de terminais e estações, de espaços adequados para empreendimentos associados e serviços públicos”.</p>
			<p>É na política de desenvolvimento urbano descrita acima que se insere o projeto de concessão dos terminais de ônibus vinculados ao Sistema de Transporte Coletivo Urbano do Município. A concessão desses equipamentos para a administração, manutenção, conservação, exploração comercial e requalificação pelo setor privado é uma estratégia capaz de maximizar os benefícios associados aos terminais do sistema de transporte coletivo.</p>
			<p>Espera-se que a concessão dos terminais a parceiros privados traga maior eficiência operacional a esses equipamentos, bem como melhorias em sua infraestrutura, proporcionando maior acessibilidade, segurança e conforto aos usuários. Ao mesmo tempo, a exploração econômical de edificações a serem construídas no terreno dos terminais, autorizada pela Lei Municipal n° 16.211/2015, traz a oportunidade de atração de novas atividades à região, com impactos no adensamento demográfico e construtivo, bem como no desenvolvimento econômico da região.</p>
			<p>Em resumo, o projeto de concessão dos terminais urbanos constitui um elemento chave de uma política de desenvolvimento urbano baseada na estruturação territorial das áreas de influência desses equipamentos públicos, entendidos como vetores de desenvolvimento urbano e econômico, ou polos de centralidade. Este conceito recebeu o nome de “Desenvolvimento Orientado ao Transporte” (TOD, da sigla em inglês para Transit Oriented Development) e refere-se ao desenvolvimento urbano compacto e à diversificação de usos próximos a grandes eixos de transporte público. A ideia-base desse desenvolvimento é maximizar as potencialidades associadas à proximidade do sistema de transporte público. Projetos concebidos pela lógica TOD têm a capacidade de reduzir deslocamentos motorizados, incentivar a alocação da população em áreas dotadas de infraestrutura e equilibrar a relação entre os locais de emprego, moradia e de outros serviços.</p>

			<h2 class="subtitle">Características básicas da proposta: um Projeto de Intervenção Urbana para a Área de Estruturação Local polarizada pelos Terminais</h2>
			<p>O Plano Diretor Estratégico estruturou a ordenação do território municipal segundo áreas homogêneas que orientam os objetivos específicos do desenvolvimento urbano e a aplicação dos instrumentos urbanísticos e ambientais. Assim, foram definidas oito Macroáreas, agrupadas em duas Macrozonas: a Macrozona de Estruturação e Qualificação Urbana, mais propícia para abrigar usos e atividades urbanos, e a Macrozona de Proteção e Recuperação Ambiental, território ambientalmente frágil, que abriga mananciais hídricos do Município e significativa biodiversidade, demandando cuidados especiais para sua conservação.</p>
			<p>Os terminais aqui estudados estão localizados na Macrozona de Estruturação e Qualificação Urbana, em Macroáreas distintas, mas que, em muitos aspectos, compartilham dos mesmos objetivos de desenvolvimento urbano.</p>
			<p>Para o Setor Central da Macroárea de Estruturação Metropolitana, onde está situado o terminal Princesa Isabel, no âmbito da Prefeitura Regional da Sé, o § 3º do Artigo 12 do PDE estabelece objetivos relacionados ao fortalecimento da centralidade municipal e à promoção da mescla de populações de diferentes faixas de renda.</p>
			<p>Para a Macroárea de Redução da Vulnerabilidade Urbana, onde estão situados os Terminais Capelinha e Campo Limpo, o PDE estabelece objetivos relacionados ao fortalecimento das capacidades de proteção social, ao incentivo de atividades não residenciais para ampliação da oferta de empregos e à prevenção ao surgimento de novas ocupações e situações de vulnerabilidade social.</p>
			<p>São comuns a ambos os territórios os objetivos de qualificação da habitação subnormal (cortiços, no Setor Central, e favelas, na Prefeitura Regional de Campo Limpo); de ampliação da oferta de habitações de interesse social; de proteção ao patrimônio ambiental e histórico; e de qualificação do transporte público.</p>
			<p>Em linha com as diretrizes do PDE, os Projetos de Intervenção Urbana elaborados para atendimento da Lei Municipal n° 16.211/2015 têm por objetivo principal o fortalecimento de centralidades polarizadas por terminais de ônibus que, integrando a rede estrutural de transporte coletivo e abrigando usos complementares, podem contribuir para a dinamização das economias locais.</p>
			<p>O fortalecimento de centralidades, como aponta o artigo 181 do PDE já mencionado, se efetiva através das seguintes ações:</p>
			<ol>
				<li>qualificação urbanística das ruas comerciais, a ser promovida preferencialmente em parcerias com a iniciativa privada, incluindo: adequação de calçadas; melhoria da iluminação pública; implantação de mobiliário urbano; sinalização visual;</li>
				<li>implantação concentrada de equipamentos públicos como elementos catalisadores do comércio e serviços privados;</li>
				<li>oferta de programas habitacionais, de assistência social, de saúde e de inclusão produtiva voltados para a população vulnerável;</li>
				<li>fortalecimento e crescimento dos polos de saúde, educação e pesquisa existentes, por meio de regulamentação urbanística que permita a sua expansão em unidades complementares no entorno.</li>
			</ol>
			<p>Dentro desse propósito de fortalecimento da centralidade polarizada pelo terminal de ônibus, o perímetro definido para o Projeto de Intervenção Urbana de cada Terminal foi estudado com base no conceito de Área de Estruturação Local (“AEL”) que, conforme Artigo 149 do PDE, corresponde a uma porção do território onde convergem ações públicas de qualificação urbanística e de promoção social, orientadas pela associação do desenvolvimento urbano à qualificação da mobilidade.</p>
			<p>O transporte público torna-se o fio condutor que estrutura ou reorganiza centralidades, ancorado ao território através de uma escala complementar de conexões, suportada por uma malha de ruas qualificadas, que integra numa rede de percursos equipamentos, centros de consumo, núcleos residenciais e espaços públicos de lazer.</p>
			<p>A definição dos percursos que fazem parte dessa malha considera: a rede de transporte coletivo, as permanências no território, os pontos de concentração de comércio/serviços, os equipamentos de uso público, o patrimônio cultural e os espaços usuais de sociabilidade local.</p>
			<p>Desta forma, dentre as diretrizes de intervenção relacionadas para o perímetro de abrangência de cada terminal, destacam-se, neste momento, aquelas voltadas à ampliação e à melhoria da conectividade local.</p>
			<p>Trata-se aqui de uma ação preliminar para qualificação e delimitação de territórios de convergência de políticas públicas – as Áreas de Estruturação Local - que será replicada em outros 24 (vinte e quatro) perímetros de abrangência dos terminais de ônibus.</p>

