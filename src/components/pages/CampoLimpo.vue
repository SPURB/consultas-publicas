			<h2>1.4.4	Impacto e Benefícios Esperados</h2>

			<p>Conforme anteriormente mencionado, a concessão dos terminais, nos termos da Lei Municipal nº 16.211/2015, está inserida numa estratégia de maximizar os benefícios relativos ao desenvolvimento associado e próximo a grandes eixos de transporte público. Tais benefícios referem-se não só às melhorias infraestruturais e operacionais pertinentes aos terminais, mas também à requalificação dos seus respectivos perímetros de abrangência.</p>

			<p>Melhorias infraestruturais e operacionais poderão beneficiar tanto funcionários como usuários dos terminais.  Funcionários da futura concessionária ou das operadoras das linhas de ônibus, poderão usufruir de melhores vestiários, banheiros, salas administrativas; áreas mais adequadas para circulação interna de veículos, equipamentos de Tecnologia de Informação atualizados, dentre outras melhorias. Ao mesmo tempo, os usuários poderão desfrutar de maior segurança, conforto, acessibilidade, limpeza, comunicação, diversidade de serviços, dentre outros aspectos.</p>

			<p>Paralelamente, a exploração econômica de edificações a serem construídas no terreno dos terminais também trará benefícios também para a população em geral. Empreendimentos associados, fundamentados na mistura de moradia, serviços (públicos e privados), comércio e outros usos, têm o potencial de atrair novos fluxos de pessoas e atividades para suas respectivas regiões. O estímulo a novas dinâmicas e atividades econômicas deve contribuir para o adensamento demográfico e construtivo, e em ultima instância para o desenvolvimento econômico do entorno mais imediato dos terminais. Complementando essa estratégia, as intervenções previstas nos PIUs promoveriam o ordenamento e a reestruturação urbana do perímetro mais abrangente de 600 metros.</p>

			<p>Se por um lado os investimentos no terreno dos terminais devem beneficiar de forma similar a operação desses equipamentos, por outro, reconhece-se que são as particularidades do entorno de cada Terminal que ditam o impacto econômico, urbanístico e social dos investimentos previstos no PIU. No caso específico do Terminal Campo Limpo, as intervenções previstas no PIU dialogam com uma diretriz central do PDE, a de desconcentrar as oportunidades de trabalho, emprego e renda, beneficiando os bairros periféricos (cf. art. 10°, § 2º , V, PDE).</p>

			<p>A transformação do perímetro do Terminal Campo Limpo deverá ocorrer através da recuperação dos espaços públicos de acesso preferencial ao Terminal e aos demais equipamentos existentes; da requalificação de logradouros, da implementação de cruzamentos de pedestres, alargamento de calçadas e guias rebaixadas; da arborização de vias; da implantação de ciclovia/ciclofaixa; dentre outras intervenções previstas no Mapa TMO012U100 Ø. Espera-se que as intervenções previstas no PIU, atreladas à requalificação do Terminal e à construção de seus empreendimentos associados, sejam capazes de potencializar a atratividade dos eixos qualificados para novas atividades econômicas, ampliar o uso residencial e acrescentar novos espaços e equipamentos públicos de qualidade. Tais transformações, em linha com as diretrizes de fortalecimento de polos e eixos de centralidades (cf. art. 181°,I, PDE), deverão contribuir para a diminuição das desigualdades na oferta e distribuição dos serviços, equipamentos e infraestruturas urbanas, verificadas entre os distritos do centro e da periferia. </p>


			<h2 class="subtitle">1.4.5	A Viabilidade do PIU e da Concessão</h2>

			<p>A conveniência da realização do PIU do Terminal Campo Limpo reside não apenas na necessidade de requalificação do Terminal e de seu perímetro de abrangência, como também no equacionamento das condições de custeio do equipamento, que, anualmente, ultrapassa os R$ 8 milhões e aufere receitas de pouco mais de R$ 300 mil.
			Presentemente, o Terminal Campo Limpo representa um ônus significativo para a Administração Pública Municipal, que não dispõe de capacidade econômico-financeira para realizar os investimentos necessários para sua ideal manutenção e operação, e, menos ainda, para novos investimentos.<sup><a href="#fn2">2</a></sup></p>

			<p>Tendo em vista tais questões, a Prefeitura Municipal de São Paulo definiu como estratégia chave a concessão do Terminal Campo Limpo para a administração, manutenção, conservação, exploração comercial e requalificação pelo setor privado.  A concessão não só desonerará a Administração Pública, como garantirá novos usos ao terreno do Terminal e melhor aproveitamento de seu potencial construtivo. Em conformidade com a Lei Municipal nº 16.211/2015, a remuneração dos serviços e dos investimentos despendidos pelo parceiro privado na administração, manutenção e requalificação do terminal deverá ser obtida através de receitas decorrentes da exploração comercial, direta ou indireta, de toda a área do terminal, de edificações a serem construídas no terreno da estação, de publicidade, bem como de outras fontes de receita que não onerem o Sistema de Transporte Coletivo Urbano de Passageiros. Em outras palavras, a exploração de receitas de empreendimentos acessórios é condição para a viabilidade econômico-financeira da concessão.</p>

			<p>É importante notar que parte do excedente econômico da concessão terá como destino a execução do PIU, que poderá ser realizado diretamente pelo futuro concessionário (art 6° da Lei Municipal nº 16.211/2015). </p>

			<p>A viabilidade econômico-financeira da concessão determinará, portanto, da capacidade do parceiro privado de implementar as intervenções previstas no PIU. Por sua vez, as intervenções que não forem passíveis de inclusão no objeto da concessão tornam-se elemento de referência obrigatória nas ações da municipalidade naquele território, orientando transformações urbanísticas futuras na região.</p>

			<h2 class="subtitle">1.4.6	Financiamento da Intervenção</h2>
<p>Os investimentos previstos pelo PIU para a requalificação do perímetro de abrangência do Terminal Campo Limpo são estimados em aproximadamente R$ 135 milhões. As intervenções poderão ser realizadas tanto com o emprego de recursos privados como públicos, conforme o art. 6º da Lei Municipal nº 16.211/2015. </p>

<p>O futuro parceiro privado será responsável pelas intervenções do PIU que forem previstas no contrato de concessão do Terminal Campo Limpo, nos termos do art. 1º, § 2º, do Decreto Municipal nº 58.066/2018. As obras a serem realizadas no perímetro de abrangência que constarão como de obrigação da concessionária serão definidas em conjunto com os demais encargos da concessão, na medida em que forem viáveis, de acordo com o modelo de econômico-financeiro do projeto. </p>

<p>Já as demais intervenções tornam-se elemento de referência nas ações futuras da  Administração Municipal naquele território. Para a sua realização, poderão ser empregados recursos provenientes das fontes consideradas mais apropriadas ou convenientes a depender da intervenção, sempre respeitando a legislação vigente. Poderão ser utilizados recursos do Fundo Municipal de Habitação (FMH), nos casos previstos no art. 10º da Lei n° 11.632/1994; do Fundo de Desenvolvimento Urbano (FUNDURB), no caso de intervenções que se enquadrem nas previstas no art. 339 da Lei n° 16.050/2014; do Fundo Municipal de Saneamento Ambiental e Infraestrutura (FMSAI), nos casos previstos no art. 6º da Lei nº 14.934/2009; do Fundo Municipal de Desenvolvimento (FMD), nos casos previstos no art. 6º da Lei nº 16.651/2017, além de outras fontes orçamentárias não vinculadas.</p>

<p>Cumpre notar que a criação da Área de Estruturação Local - AEL Campo Limpo tem por objetivo estabelecer o Programa de Intervenções propostas no PIU entre as prioridades de investimento de recursos do FUNDURB, nos termos do § 3.º do art. 11 de seu decreto regulamentador, Decreto Municipal nº 57.547/ 2016: </p>

<blockquote>
	<p><i>
		“Art. 11 [...]
§ 3º Em complementação às prioridades previstas neste artigo<sup><a href="#fn3">3</a></sup>, terão relevância os investimentos propostos nas Redes de Estruturação e Transformação Urbana, preferencialmente nos Eixos de Estruturação da Transformação Urbana e nas Macroáreas de Estruturação Metropolitana, de Qualificação da Urbanização, de Redução da Vulnerabilidade Urbana, de Redução da Vulnerabilidade e Recuperação Ambiental, de Controle e Qualificação Urbana e Ambiental, de Contenção Urbana e Uso Sustentável e de Preservação de Ecossistemas Naturais, de acordo com os seguintes critérios:
