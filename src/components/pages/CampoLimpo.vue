<p>Desta maneira, será possível pleitear que valores captados através da outorga onerosa de potencial adicional de construção, no âmbito da AEL Campo Limpo, sejam investidos na implantação do PIU Campo Limpo.</p>

<h2 class="subtitle">1.4.7	Modelo de Gestão</h2>

<p>O Decreto nº 56.901 de 29 de março de 2016 que disciplina a elaboração dos Projetos de Intervenção Urbana assegura a realização de consultas públicas em etapas definidas de sua elaboração, para discussão e aperfeiçoamento das propostas. Da mesma maneira, a Minuta de Decreto de implantação do PIU é colocada em consulta pública prévia, procedimento que será também adotado para o Decreto de Implantação do PIU Campo Limpo.
Para a gestão participativa do PIU, na sua etapa de implantação, propõe-se o recurso aos órgãos colegiados já constituídos.</p>

<p>Cada Prefeitura Regional conta com um Núcleo Regional de Planejamento, constituído por técnicos das diversas Secretarias Municipais, que, nos termos do § 1º do art. 324 do PDE,  atua “como um colegiado intersetorial e interdisciplinar, responsável pela integração e articulação das políticas setoriais bem como pelo acompanhamento das ações voltadas à concretização dos princípios diretrizes e ações do Plano Diretor, dos Planos Regionais e dos Planos de Desenvolvimento de Bairro”.</p>

<p>Os NRP foram constituídos e seus integrantes designados pela Portaria SMPR Nº 31 de 7 de Julho de 2017, publicada pela Secretaria Municipal das Prefeituras Regionais.</p>

<p>O Decreto nº 57.537, de 16 de dezembro de 2016, dispõe em seu Art. 7º, inciso V, que os Núcleos Regionais de Planejamento – NRP, entre outras atribuições, devem “constituir subgrupos para acompanhamento de intervenções específicas”, arranjo compatível com o acompanhamento da implantação do Projeto de Intervenção Urbana Campo Limpo, que terá fases bem definidas de implantação e determinadas pela disponibilidade de recursos, não assegurados na concessão.</p>

<p>Do lado dos moradores, os artigos 34 e 35 da Lei nº 15.764 de 27 de maio de 2013 dispõem sobre a constituição e atribuições dos Conselhos Participativos Municipais, dentre as quais “zelar para que os direitos da população e os interesses públicos sejam atendidos nos serviços, programas e projetos públicos da região e comunicar oficialmente aos órgãos competentes em caso de deficiências neste atendimento”.</p>

<p>O Decreto nº 56.208, de 30 de Junho de 2015, que regulamenta os dois artigos citados, estabelece o número de conselheiros por Distrito e o número máximo 51 conselheiros por Prefeitura Regional, eleitos por votação secreta para um mandato de 2 anos.</p>

<p>Considerando que o PIU Campo Limpo abrange áreas da Prefeitura Regional de mesmo nome, propomos a seguinte composição para o Grupo Gestor:</p>

<ul>
	<li>a.	5 integrantes do Conselho Participativo da Prefeitura Regional do Campo Limpo;</li>
	<li>b.	3  integrantes do Núcleo Regional de Planejamento da Prefeituras Regional do Campo Limpo;</li>
	<li>c.	1 representante da São Paulo Urbanismo e</li>
	<li>d.	 1 representante do Concessionário do Terminal do Campo Limpo.</li>
</ul>
<p>As atribuições e o funcionamento desse Grupo serão estabelecidos por Regimento Interno, elaborado pela São Paulo urbanismo e aprovado pela maioria de seus integrantes.</p>

