					<p>O adensamento populacional pode ocorrer, conforme os <strong>cenários B e C</strong>, com a substituição parcial ou total dos usos institucionais atuais por usos residenciais monofuncionais ou empreendimentos mistos, podendo mesmo não ocorrer, caso a demanda do mercado imobiliário da região direcione os novos empreendimentos para atividades não residenciais. </p>

