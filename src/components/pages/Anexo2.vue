							<li>IV - incentivar o melhor aproveitamento dos imóveis, em particular dos não construídos ou subutilizados;</li>
							<li>V - ampliar e articular os espaços de uso público, em particular os arborizados e destinados à circulação e bem-estar dos pedestres.</li>					
