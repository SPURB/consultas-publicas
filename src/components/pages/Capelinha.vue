			<p>Os problemas operacionais e infraestruturais do Terminal Capelinha contribuem para que esse equipamento público não atue como um centro dinamizador do seu entorno. Apesar de mais de 57 mil passageiros embarcarem diariamente no Terminal, a região pouco se beneficia do potencial que esse fluxo de pessoas pode trazer. O entorno de Terminal Capelinha caracteriza-se por ser uma área ocupada por população predominantemente de baixa renda, baixa densidade construtiva e déficit na oferta de serviços, equipamentos e infraestruturas urbanas. Nesse sentido, a requalificação desse Terminal, no contexto da Lei Municipal nº 16.211/2015, e a efetivação das intervenções previstas no seu PIU, têm o potencial de transformar esse equipamento numa âncora de desenvolvimento na zona sul de São Paulo.</p>

