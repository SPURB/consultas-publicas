			<p>Para a delimitação desses polígonos, foram observados limites das zonas estabelecidas na Lei 16.402/16, possibilidade de inclusão de terrenos vagos ou áreas passíveis de transformação – tendo em vista a ampliação do potencial estruturante da intervenção - e, inversamente, exclusão de áreas consolidadas que já desfrutassem de bom padrão de urbanização.</p>
