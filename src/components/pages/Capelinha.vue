			<h3 class="subtitle">1.4.3	Fases de implantação</h3>

			<p>O Projeto de Intervenção Urbana está associado à concessão dos terminais de ônibus urbanos que tem como um dos objetivos a administração, manutenção, conservação, exploração comercial e requalificação do terminal e de seus empreendimentos associados.</p>

			<p>Considerado no âmbito da política pública de desenvolvimento urbano descentralizado do Município, que dá suporte ao plano de concessão de 27 terminais de ônibus, o ponto focal a partir de onde se define a Área de Estruturação Local é o próprio terminal. </p>

			<p>Assim, a fase inicial de implantação do PIU atinge o conjunto de intervenções previstas para o terreno do terminal, abrangendo, obrigatoriamente, a requalificação do terminal e a reserva de área não inferior a 25 m² para serviço público dentre as atividades auxiliares previstas no § 2º do art. 107 da Lei 16.402/2016, e, facultativamente, áreas adicionais para empreendimentos associados para categorias de uso autorizadas no Quadro 2, anexo.</p>

			<p>O elenco de intervenções urbanísticas propostas no PIU será de observância obrigatória pelo Poder Público, quando de sua atuação no perímetro da AEL que contém o terminal concedido, e de execução obrigatória pelos concessionários, quando previstas no contrato de concessão.</p>

			<p>A magnitude do valor das intervenções previstas no PIU indica que parte significativa do plano proposto será realizada por recursos externos ao contrato de concessão.</p>

			<p>A priorização das demais intervenções deverá ser proposta pela São Paulo Urbanismo, responsável pela implantação do PIU, em conjunto com o Núcleo Regional de Planejamento, vinculado à Prefeitura Regional específica, instituído nos termos do § 1º do art. 324 do PDE e regulamentado pelo Decreto nº 57.537/ 2016 e debatida em conjunto com os Conselhos Participativos das Prefeituras Regionais.</p>

			<h3 class="subtitle">1.4.4	Impacto e Benefícios Esperados</h3>

			<p>Conforme anteriormente mencionado, a concessão dos terminais, nos termos da Lei Municipal nº 16.211/2015, está inserida numa estratégia de maximizar os benefícios relativos ao desenvolvimento associado e próximo a grandes eixos de transporte público. Tais benefícios referem-se não só às melhorias infraestruturais e operacionais pertinentes aos terminais, mas também à requalificação dos seus respectivos perímetros de abrangência.</p>
			<p>Melhorias infraestruturais e operacionais poderão beneficiar tanto funcionários como usuários dos terminais.  Funcionários, da futura concessionária ou das operadoras das linhas de ônibus, poderão usufruir de melhores vestiários, banheiros, salas administrativas; áreas mais adequadas para circulação interna de veículos, equipamentos de Tecnologia de Informação atualizados, dentre outras melhorias. Ao mesmo tempo, os usuários poderão desfrutar de maior segurança, conforto, acessibilidade, limpeza, comunicação, diversidade de serviços, dentre outros aspectos.</p>
			<p>Paralelamente, a exploração comercial de edificações a serem construídas no terreno dos terminais também trará benefícios também para a população em geral. Empreendimentos associados, fundamentados na mistura de moradia, serviços (públicos e privados), comércio e outros usos, têm o potencial de atrair novos fluxos de pessoas e atividades para suas respectivas regiões. O estímulo a novas dinâmicas e atividades econômicas deve contribuir para o adensamento demográfico e construtivo, e em ultima instância para o desenvolvimento econômico do entorno mais imediato dos terminais. Complementando essa estratégia, as intervenções previstas nos PIUs promoveriam o ordenamento e a reestruturação urbana do perímetro mais abrangente de 600 metros.</p>

			<p>Se por um lado os investimentos no terreno dos terminais devem beneficiar de forma similar a operação desses equipamentos, por outro lado reconhece-se que são as particularidades do entorno de cada Terminal que ditam o impacto econômico, urbanístico e social dos investimentos previstos no PIU. No caso específico do Terminal Capelinha, as intervenções previstas no PIU dialogam com uma diretriz central do PDE, a de desconcentrar as oportunidades de trabalho, emprego e renda, beneficiando os bairros periféricos (cf. art. 10°, § 2º , V, PDE). </p>
			<p>A transformação do perímetro do Terminal Capelinha deverá ocorrer através  da recuperação dos espaços públicos de acesso preferencial ao Terminal e aos demais equipamentos existentes; da requalificação de logradouros, da implementação de cruzamentos de pedestres, alargamento de calçadas e guias rebaixadas; da arborização de vias; da implantação de ciclovia/ciclofaixa; dentre outras intervenções previstas no Mapa TMO012U200 Ø. Espera-se que tais intervenções previstas no PIU, atreladas à requalificação do Terminal e à construção de seus empreendimentos associados, sejam capazes de potencializar atividades comerciais na área, atrair novas atividades econômicas, requalificar o uso residencial e acrescentar novos espaços e equipamentos públicos de qualidade. Tais transformações, em linha com as diretrizes de fortalecimento de polos e eixos de centralidades (cf. art. 181°,I, PDE), deverão contribuir para a diminuição das desigualdades na oferta e distribuição dos serviços, equipamentos e infraestruturas urbanas entre os distritos do centro e da periferia.</p>

			<h3 class="subtitle">1.4.5 A Viabilidade do PIU e da Concessão</h3>
			<p>A conveniência da realização do PIU do Terminal Capelinha reside não apenas na necessidade de requalificação do Terminal e de seu perímetro de abrangência, como também na condição financeira não sustentável desse equipamento que traz altos custos para a Municipalidade. Com um custo anual de mais de R$ 12.000.000,00 (doze milhões de reais) e receitas anuais de pouco mais de R$ 600.000 (seiscentos milhões de reais), o Terminal Capelinha torna-se muito oneroso para a Administração Pública Municipal, que já não dispõe de capacidade econômico-financeira para realizar os investimentos necessários para sua manutenção e operação, tampouco para novos investimentos . </p>
			<p>Tendo em vista as questões levantadas acima, a Prefeitura Municipal de São Paulo definiu como estratégia chave a concessão do Terminal Capelinha para a administração, manutenção, conservação, exploração comercial e requalificação pelo setor privado.  A concessão não só desonerará a Administração Pública, como garantirá novos usos ao terreno do Terminal e melhor aproveitamento de seu potencial construtivo. Em conformidade com a Lei Municipal nº 16.211/2015, a remuneração dos serviços e dos investimentos despendidos pelo parceiro privado na administração, manutenção e requalificação do terminal deverão ser obtidas através de receitas decorrentes da exploração comercial, direta ou indireta, de toda a área do terminal, de edificações a serem construídas no terreno da estação, de publicidade, bem como de outras fontes de receita que não onerem o Sistema de Transporte Coletivo Urbano de Passageiros. Em outras palavras, a exploração de receitas de empreendimentos acessórios é condição para a viabilidade econômico-financeira da concessão.</p>
			<p>É importante notar que parte do excedente econômico da concessão / o ônus da concessão terá como destino a execução do PIU, que poderá ser realizado diretamente pelo futuro concessionário (Art 6°, Lei Municipal nº 16.211/2015). A viabilidade econômico-financeira da concessão determinará, portanto, da capacidade do parceiro privado de implementar as intervenções previstas no PIU. Por sua vez, as intervenções que não forem passíveis de inclusão no objeto da concessão tornam-se elemento de referência obrigatória nas ações da municipalidade naquele território, orientando transformações urbanísticas futuras na região.</p>

			<h3 class="subtitle">1.4.6	Financiamento da Intervenção</h3>
			<p>Os investimentos previstos pelo PIU para a requalificação do perímetro de abrangência do Terminal Capelinha  são estimados em R$ 83 milhões. As intervenções poderão ser realizadas tanto com o emprego de recursos privados como públicos, conforme o art. 6º da Lei Municipal nº 16.211/2015. </p>
			<p>O futuro parceiro privado será responsável pelas intervenções do PIU que forem previstas no contrato de concessão do Terminal Capelinha, nos termos do art. 1º, § 2º, do Decreto Municipal nº 58.066/2018. As obras a serem realizadas no perímetro de abrangência que constarão como de obrigação da concessionária serão definidas em conjunto com os demais encargos da concessão, na medida em que forem viáveis, de acordo com o modelo de econômico-financeiro do projeto. </p>
			<p>Já as demais intervenções tornam-se elemento de referência nas ações futuras da  Administração Municipal naquele território. Para a sua realização, poderão ser empregados recursos provenientes das fontes consideradas mais apropriadas ou convenientes a depender da intervenção, sempre respeitando a legislação vigente. Poderão ser utilizados recursos do Fundo Municipal de Habitação (FMH), nos casos previstos no art. 10º da Lei n° 11.632/1994; do Fundo de Desenvolvimento Urbano (FUNDURB), no caso de intervenções que se enquadrem nas previstas no art. 339º da Lei n° 16.050/2014; do Fundo Municipal de Saneamento Ambiental e Infraestrutura (FMSAI), nos casos previstos no art. 6º da Lei nº 14.934/2009; do Fundo Municipal de Desenvolvimento (FMD), nos casos previstos no art. 6º da Lei nº 16.651/2017, além de outras fontes orçamentárias não vinculadas.</p>
			<p>Cumpre notar que a criação da Área de Estruturação Local - AEL Capelinha tem por objetivo estabelecer o Programa de Intervenções propostas no PIU entre as prioridades de investimento de recursos do FUNDURB, nos termos do Parágrafo 3.º do art. 11 de seu decreto regulamentador, Decreto Municipal nº 57.547/ 2016: </p>
			<blockquote>
				<p><i>“Art. 11 [...]
				§ 3º Em complementação às prioridades previstas neste artigo  , terão relevância os investimentos propostos nas Redes de Estruturação e Transformação Urbana, preferencialmente nos Eixos de Estruturação da Transformação Urbana e nas Macroáreas de Estruturação Metropolitana, de Qualificação da Urbanização, de Redução da Vulnerabilidade Urbana, de Redução da Vulnerabilidade e Recuperação Ambiental, de Controle e Qualificação Urbana e Ambiental, de Contenção Urbana e Uso Sustentável e de Preservação de Ecossistemas Naturais, de acordo com os seguintes critérios:
				[...]
				III - pedidos que se enquadrem como Projetos de Intervenção Urbana - PIU ou Áreas de Estruturação Local - AEL, conforme estabelece a Lei nº 16.050, de 2014;[...]”
				</i></p>
			</blockquote>
			<p>Desta maneira, será possível pleitear que valores captados através da outorga onerosa de potencial adicional de construção, no âmbito da AEL Capelinha, sejam investidos na implantação do PIU.</p>
			
			<h3 class="subtitle">1.4.7	Modelo de Gestão</h3>
			<p><i class="strong">O Decreto nº 56.901 de 29 de março de 2016</i> que disciplina a elaboração dos Projetos de Intervenção Urbana assegura a realização de consultas públicas em etapas definidas de sua elaboração, para discussão e aperfeiçoamento das propostas. Da mesma maneira, a Minuta de Decreto de implantação do PIU é colocada em consulta pública prévia, procedimento que será também adotado para o Decreto de Implantação do PIU Capelinha.</p>

			<p>Para a gestão participativa do PIU, na sua etapa de implantação, propõe-se o recurso aos órgãos colegiados já constituídos.</p>
			
			<p>Cada Prefeitura Regional conta com um <i class="strong">Núcleo Regional de Planejamento</i>, constituído por técnicos das diversas Secretarias Municipais, que, <i class="strong">nos termos do § 1º do Art. 324 do PDE</i>,  atua “como um colegiado intersetorial e interdisciplinar, responsável pela integração e articulação das políticas setoriais bem como pelo acompanhamento das ações voltadas à concretização dos princípios diretrizes e ações do Plano Diretor, dos Planos Regionais e dos Planos de Desenvolvimento de Bairro”.</p>

			<p>Os NRP foram constituídos e seus integrantes designados pela Portaria SMPR Nº 31 de 7 de Julho de 2017, publicada pela Secretaria Municipal das Prefeituras Regionais.</p>

			<p>O <i class="strong">Decreto nº 57.537, de 16 de dezembro de 2016</i>, dispõe em seu art. 7º, inciso V, que os Núcleos Regionais de Planejamento – NRP, entre outras atribuições, devem “constituir subgrupos para acompanhamento de intervenções específicas”, arranjo compatível com o acompanhamento da implantação do Projeto de Intervenção Urbana Capelinha, que terá fases bem definidas de implantação e determinadas pela disponibilidade de recursos, não assegurados na concessão.</p>

			<p>Do lado dos moradores, os <i class="strong">artigos 34 e 35 da Lei nº 15.764 de 27 de maio de 2013</i> dispõem sobre a constituição e atribuições dos Conselhos Participativos Municipais, dentre as quais “zelar para que os direitos da população e os interesses públicos sejam atendidos nos serviços, programas e projetos públicos da região e comunicar oficialmente aos órgãos competentes em caso de deficiências neste atendimento”.</p>

			<p>O <i class="strong">Decreto nº 54.156, de 1º de agosto de 2013</i>, que regulamenta os dois artigos citados, estabelece o número de conselheiros por Distrito e o número máximo 51 conselheiros por Prefeitura Regional, eleitos por votação secreta para um mandato de 2 anos.
			Considerando que o PIU Capelinha abrange áreas da Prefeitura Regional do Campo Limpo, propomos a seguinte composição para o Grupo Gestor:</p>
			<ul>
				<li>a.	5 integrantes do Conselho Participativo da Prefeitura Regional de Campo Limpo;</li>
				<li>b.	3  integrantes do Núcleo Regional de Planejamento da Prefeituras Regional de Campo Limpo;</li>
				<li>c.	1 representante da São Paulo Urbanismo e</li>
				<li>d.	1 representante do Concessionário do Terminal Capelinha.</li>
 			</ul>
 			<p>As atribuições e o funcionamento desse Grupo serão estabelecidos por Regimento Interno, elaborado pela São Paulo urbanismo e aprovado pela maioria de seus integrantes.</p>
