				<section id="Cap08">
					<h6> 1.7 Habitação de Interesse Social </h6>
					<p>Como uma das regiões mais urbanizadas e consolidadas da cidade, a AII há muito tempo foi se desenvolvendo e adensando e, à medida em que ia se valorizando, a população mais vulnerável migrava para outras localidades. Mesmo os cortiços presentes no núcleo mais antigo de Pinheiros, próximo ao Largo da Batata, já não existem. Os núcleos remanescentes se localizam na Vila Madalena.</p>

