					<p>Os lançamentos voltados a empreendimentos não residenciais tiveram uma evolução bem mais discreta nos últimos anos, concentrando-se nas proximidades da linha 4 amarela do Metrô, sobretudo no entorno do Largo da Batata e das ruas Paes Leme, dos Pinheiros e Artur de Azevedo.</p>

