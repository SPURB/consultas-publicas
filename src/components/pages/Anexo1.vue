					<p>Contudo, o elemento mais relevante sobre este tema é certamente a Praça Victor Civita, um caso considerado bem-sucedido de reabilitação de áreas contaminadas. Antigo depósito de lixo e medicamentos e Incinerador de Lixo de Pinheiros, a área foi objeto de reabilitação e de um cuidadoso projeto que, além de restituir à cidade um equipamento ambiental, de lazer, esporte e cultura, soube tirar proveito do tema da reabilitação de áreas contaminadas para agregar ao programa atividades educativas, utilizando o próprio edifício onde funcionou o incinerador como testemunha do uso pregresso e das novas possibilidades de utilização ancoradas na sustentabilidade.</p>

