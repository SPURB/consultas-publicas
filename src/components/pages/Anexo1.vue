					<p>O art. 10, §2º do PDE dispõe sobre os objetivos da Macrozona de Estruturação e Qualificação Urbana, descritos a seguir:</p>

					<blockquote>
						<ul>
							<li>I - promoção da convivência mais equilibrada entre a urbanização e a conservação ambiental, entre mudanças estruturais provenientes de grandes obras públicas e privadas e as condições de vida dos moradores; </li>
							<li>II - compatibilidade do uso e ocupação do solo com a oferta de sistemas de transporte coletivo e de infraestrutura para os serviços públicos;</li>
							<li>III - orientação dos processos de reestruturação urbana de modo a repovoar os espaços com poucos moradores, fortalecer as bases da economia local e regional, aproveitar a realização de investimentos públicos e privados em equipamentos e infraestruturas para melhorar as condições dos espaços urbanos e atender necessidades sociais, respeitando as condicionantes do meio físico e biótico e as características dos bens e áreas de valor histórico, cultural, religioso e ambiental; </li>
							<li>IV - eliminação e redução das situações de vulnerabilidades urbanas que expõem diversos grupos sociais, especialmente os de baixa renda como pessoas em situação de rua, catadores e trabalhadores ambulantes, a situações de riscos, perigos e ameaças;
							</li> 
							<li>V - diminuição das desigualdades na oferta e distribuição dos serviços, equipamentos e infraestruturas urbanas entre os distritos;</li> 
							<li>VI - desconcentração das oportunidades de trabalho, emprego e renda, beneficiando os bairros periféricos;</li>
							<li>VII - manutenção, proteção e requalificação das zonas exclusivamente residenciais consideradas as disposições dos arts. 27 e 33 desta lei.</li>
						</ul>

					</blockquote>

