					<h5> 2. Análise ambiental </h5>
					<h6 id="Cap12"> 2.1 Meio Físico </h6>
					<p>O PIU Sumidouro está localizado na planície aluvial do Rio Pinheiros, apresentando uma topografia regular e plana. Parte da área, correspondente aos lotes 0001 e 0002, continha trecho do curso original do Rio Pinheiros, retificado posteriormente. Na face noroeste, o Córrego das Corujas, que nasce na Vila Beatriz, passa sob o leito da Av. Prof. Frederico Hermann Jr.</p>

					<div class="figure">
					<img :src="require('../../assets/img/anexo1/image29.jpeg')" class="responsive-img" data-caption="Figura 28: Bacias Hidrográficas"/>	
					<p><strong>Figura 27: Relevo e hidrografia 1930. </strong>. Fonte: MDC 2004 e Sara Brasil, 1930.</p>
					</div>
					

					<p>As bacias hidrográficas atuais pouco diferem da estrutura de 1930. A maior parte da AID é drenada pelos córregos das Corujas e Verde I, situados nas porções nordeste e leste, enquanto as porções sul e oeste da área (Rua Sumidouro, Av. das Nações Unidas e Butantã) são tributárias diretas do Rio Pinheiros.</p>

					<div class="figure">
					<img :src="require('../../assets/img/anexo1/image30.jpeg')" class="responsive-img" data-caption="Figura 28: Bacias Hidrográficas"/>	
					<p><strong>Figura 28: Bacias Hidrográficas. </strong>Fonte: MDC 2004, PDE 2014 e FCTH 2016.</p>
					</div>

					<p>A partir da Carta Geotécnica, vê-se que a porção do PIU Sumidouro mais próxima ao Rio Pinheiros, correspondente à maior parte dos lotes 0001 e 0002 encontra-se em Terra mole e Solo compressível, enquanto que o restante da área encontra-se na planície aluvial.</p>

					<div class="figure"><img :src="require('../../assets/img/anexo1/image31.jpeg')" class="responsive-img" data-caption="Figura 29: Carta Geotécnica"/>	
					<p><strong>Figura 29: Carta Geotécnica. </strong>Fonte: MDC 2004, Emplasa 2003.</p>
					</div>

					<p>A AID contém poucas áreas verdes em seu perímetro, constituídas principalmente por praças. Além da Praça Victor Civita, situada na ADA, as mais relevantes estão situadas na ZOD Alto de Pinheiros: Praça Panamericana, Praça Cel. Custódio Fernandes Pinheiros (do Pôr do Sol) e Praça Conde de Barcelos. O único parque planejado dentro da AID está localizado na ZOD Butantã, à R. Santanésia. Já a AII está situada entre grandes áreas verdes sob governança estadual, constituídas pelos Parques Villa Lobos e Cândido Portinari, a norte, pela Cidade Universitária da USP, a oeste e pelo Parque do Povo, a sul (situado no Distrito do Itaim Bibi).</p>

					<p>Por outro lado, a presença dos bairros jardins estritamente residenciais nas ZODs Alto de Pinheiros e Butantã é um componente fundamental da qualidade ambiental da região da AID, contendo farta arborização viária e dentro dos lotes, bem como praças de menor porte e canteiros que contribuem significativamente para a preservação da avifauna, para a detenção das águas pluviais e para o retardamento do escoamento superficial. </p>

					<div class="figure"><img :src="require('../../assets/img/anexo1/image32.jpeg')" class="responsive-img" data-caption="Figura 30: Áreas verdes"/>	
					<p><strong>Figura 30: Áreas verdes. </strong>Fonte: MDC 2004, PDE 2014.</p>
					</div>

					<p>A sensação térmica é outro indicador que reflete a qualidade ambiental na AID em função dos atributos dos bairros jardins, como maior sombreamento das superfícies e maior umidade relativa do ar, entre outros. Na figura 31 é possível visualizar menores temperaturas aparentes de superfície nas regiões onde estes bairros se situam em relação a outras áreas da cidade. No presente caso, enquanto a ZOD Alto de Pinheiros apresenta um índice médio de cerca de 27°C, a ZOD Pinheiros apresenta um índice médio de 29°C.</p>	

					<div class="figure"><img :src="require('../../assets/img/anexo1/image33.jpeg')" class="responsive-img" data-caption="Figura 30: Áreas verdes"/>	
					<p><strong>Figura 31: Temperatura Aparente de Superfície. </strong>Fonte: Atlas Ambiental MSP, 2012.</p>
					</div>
